// === Helper to get icon by ID ===
export const getWalletIcon = (id: string) => {
    switch (id) {
        case 'bitget': return <IconBitget />;
        case 'imtoken': return <IconImToken />;
        case 'trust': return <IconTrustWallet />;
        case 'phantom': return <IconPhantom />;
        case 'coinbase': return <IconCoinbase />;
        case 'coolwallet': return <IconCoolWallet />;
        case 'binance': return <IconBinance />;
        case 'kucoin': return <IconKuCoin />;
        case 'gate': return <IconGate />;
        case 'okx': return <IconOKX />;
        case 'metamask': return <img src="https://upload.wikimedia.org/wikipedia/commons/3/36/MetaMask_Fox.svg" alt="MetaMask" className="w-full h-full object-contain" />;
        case 'walletconnect': return <img src="https://raw.githubusercontent.com/WalletConnect/walletconnect-assets/master/Logo/Blue%20(Default)/Logo.svg" alt="WC" className="w-full h-full object-contain" />;
        case 'tronlink': return <IconTron />;
        default: return null;
    }
};

